package com.cwtbatch.lesson;

public class Arithmetic {
    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 3;

        System.out.println("10+3(+): "+(num1+num2));
        System.out.println("10+3(-): "+ (num1-num2));
        System.out.println("10+3(*): "+num1*num2);
        System.out.println("10+3(/): "+num1/num2);
        System.out.println("10+3(%): "+num1%num2);


    }
}

package com.cwtbatch.lesson;

public class Operator {
    public static void main(String[] args) {

        int num1=10;
        int num2=2;

        System.out.println(num1>num2);
        System.out.println(num1<num2);
        System.out.println(num1==num2);
        System.out.println(num1>=num2);
        System.out.println(num1<=num2);
        System.out.println(num1!=num2);
    }
}

